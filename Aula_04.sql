	COD_PROD NUMERIC PRIMARY KEY,
    NOME_PROD VARCHAR(50),
    PESO_PROD NUMERIC,
    UNIDMEDIDA_PROD VARCHAR(50),
    VALIDADE DATE,
    VALOR NUMERIC(10,2)
);

